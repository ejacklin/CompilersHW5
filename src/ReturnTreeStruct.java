public class ReturnTreeStruct {

    public double value;
    public boolean isInt;

    public ReturnTreeStruct(double value, boolean isInt) {
        this.value = value;
        this.isInt = isInt;
    }

    public ReturnTreeStruct() {
        this.value = 0;
        this.isInt = true;
    }
}
